int		ft_atoi(char const *s)
{
	int		n;
	int		neg;

	n = 0;
	neg = 0;
	while ((*s >= '\t' && *s <= '\r') || *s == ' ')
		s++;
	if ((*s == '-') || (*s == '+'))
	{
		if (*s == '-')
			neg = 1;
		s++;
	}
	while ((*s >= '0') && (*s <= '9'))
	{
		n = n * 10;
		if (neg == 1)
			n = n - (*s - 48);
		else
			n = n + (*s - 48);
		s++;
	}
	return (n);
}
